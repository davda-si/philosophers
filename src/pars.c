#include "../philo.h"

int	pars(char **av)
{
	int	i;

	i = 1;
	while (av[i])
	{
		if (ft_invalid(av[i]))
			return (1);
		if (ft_atol(av[i]) <= 0 || ft_atol(av[i]) > INT_MAX)
			return (1);
		i++;
	}
	return (0);
}
